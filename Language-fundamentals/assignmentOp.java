

class AssignmentOp {

    public static void main(String[] args) {  
        int a = 12;
        int b = 10;

        a +=b;
        System.out.println(a);
        a -=b;
        System.out.println(a);
        a *=b;
        System.out.println(a);
        a /=b;
        System.out.println(a);
    }
}
